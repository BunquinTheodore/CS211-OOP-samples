class FloatData{
    public static void main(String[]args){
        double num1 = 17.4;
        float num2 = 93.5f;
        int num3 = 103;
        System.out.println("num1: " + num1);
        System.out.println("num2: " + num2);
        System.out.println("num3: " + num3);
    }
}

/*
    OUTPUT
    num1: 17.4
    num2: 93.5
    num3: 103

 */